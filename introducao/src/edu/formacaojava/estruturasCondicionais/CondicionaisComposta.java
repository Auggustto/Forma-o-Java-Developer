package edu.formacaojava.estruturasCondicionais;

public class CondicionaisComposta {

    public static void main(String[] args) {
        
        double nota = 2.0;

        if (nota >= 7.0) {
            System.out.println("Aprovado: " + nota);
        }
        else {
            System.out.println("Reprovado: " + nota);
        }
    }
}