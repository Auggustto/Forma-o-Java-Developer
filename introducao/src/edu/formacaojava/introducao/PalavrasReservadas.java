package edu.formacaojava.introducao;

public class PalavrasReservadas {

    //  Em Java, palavras reservadas são palavras que têm um significado especial na 
    //  linguagem e não podem ser usadas como identificadores (nomes de variáveis, métodos, classes, etc.).

    public static void main(String[] args) {
        
        // 1. abstract -> Declara uma classe ou método que deve ser implementado por subclasses. Classes abstratas não podem ser instanciadas diretamente.
        // 2. assert -> Usado para criar asserções, que são testes lógicos que podem ser ativados ou desativados em tempo de execução.
        // 3. boolean -> Define um tipo de dado booleano que pode ter um valor de `true` ou `false`.
        // 4. break -> Interrompe a execução de um loop ou de um bloco de código `switch`.
        // 5. byte -> Define um tipo de dado inteiro de 8 bits.
        // 6. case -> Define um bloco de código dentro de uma estrutura `switch` que é executado quando o valor da expressão `switch` corresponde ao valor da `case`.
        // 7. catch -> Captura exceções lançadas por um bloco `try` e lida com elas.
        // 8. char -> Define um tipo de dado de caractere de 16 bits.
        // 9. class -> Declara uma classe.
        // 10. const -> Palavra reservada, mas não usada em Java. Pode ser usada para futuras extensões.
        // 11. continue -> Pula o restante do código dentro de um loop e volta ao início do loop para a próxima iteração.
        // 12. default -> Define o bloco de código padrão em uma estrutura `switch` que é executado se nenhum dos casos corresponder.
        // 13. do -> Inicia um loop `do-while`, onde o bloco de código é executado pelo menos uma vez.
        // 14. double -> Define um tipo de dado de ponto flutuante de precisão dupla de 64 bits.
        // 15. else -> Define o bloco de código a ser executado se a condição de um `if` for falsa.
        // 16. enum -> Declara um conjunto de constantes relacionadas.
        // 17. extends -> Indica que uma classe herda de outra classe ou que uma interface estende outra interface.
        // 18. final -> Declara que um valor não pode ser alterado. Pode ser usado para variáveis, métodos e classes.
        // 19. finally -> Define um bloco de código que será sempre executado após o bloco `try` e `catch`, independentemente de ocorrer uma exceção.
        // 20. float -> Define um tipo de dado de ponto flutuante de precisão simples de 32 bits.
        // 21. for -> Inicia um loop `for`.
        // 22. goto -> Palavra reservada, mas não usada em Java. Pode ser usada para futuras extensões.
        // 23. if -> Define uma estrutura condicional.
        // 24. implements -> Indica que uma classe implementa uma interface.
        // 25. import -> Importa classes ou pacotes para o arquivo Java.
        // 26. instanceof -> Testa se um objeto é uma instância de uma classe ou implementa uma interface.
        // 27. int -> Define um tipo de dado inteiro de 32 bits.
        // 28. interface -> Declara uma interface.
        // 29. long -> Define um tipo de dado inteiro de 64 bits.
        // 30. native -> Declara que um método é implementado em código nativo (fora do Java).
        // 31. new -> Cria novas instâncias de objetos.
        // 32. null -> Representa a ausência de um valor ou referência nula.
        // 33. package -> Declara um pacote ao qual a classe pertence.
        // 34. private -> Declara que um membro da classe é acessível apenas dentro da própria classe.
        // 35. protected -> Declara que um membro da classe é acessível dentro do mesmo pacote e por subclasses.
        // 36. public -> Declara que um membro da classe é acessível de qualquer lugar.
        // 37. return -> Sai de um método e opcionalmente retorna um valor.
        // 38. short -> Define um tipo de dado inteiro de 16 bits.
        // 39. static -> Declara que um membro pertence à classe e não a uma instância específica da classe.
        // 40. strictfp -> Declara que a precisão dos cálculos de ponto flutuante deve ser estritamente de acordo com o padrão IEEE 754.
        // 41. super -> Refere-se à superclasse imediata do objeto atual e pode ser usado para acessar métodos e construtores da superclasse.
        // 42. switch -> Declara uma estrutura de controle de fluxo baseada em valores.
        // 43. synchronized -> Declara que um método ou bloco de código deve ser sincronizado, garantindo acesso exclusivo a um recurso compartilhado.
        // 44. this -> Refere-se à instância atual do objeto.
        // 45. throw -> Lança uma exceção.
        // 46. throws -> Declara que um método pode lançar uma exceção.
        // 47. transient -> Declara que um membro não deve ser serializado.
        // 48. try -> Define um bloco de código que pode lançar exceções.
        // 49. void -> Declara que um método não retorna nenhum valor.
        // 50. volatile -> Declara que um campo pode ser acessado por múltiplas threads simultaneamente.
        // 51. while -> Inicia um loop `while`, onde o bloco de código é executado enquanto a condição for verdadeira.
    }
}
