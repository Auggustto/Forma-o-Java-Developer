//  Convenção: CamelCase, com a primeira letra maiúscula.
public class Nomenclaturas {

    // Nomes de Métodos: Convenção camelCase, com a primeira letra minúscula.
    public static void main (String[] args) {

        // Nomes de Variáveis Convenção: camelCase, com a primeira letra minúscula.
        String nomeCompleto = "Jane Aparecida";
        System.out.print(nomeCompleto);

        // Nomes de Constantes Convenção: UPPER_CASE com palavras separadas por underscores.
        final double PI = 3.14159;
        System.out.println(PI);
    }
}